test("generated postgres copyfrom function waits for write backpressure", async () => {
  const { copyAuthors } = loadGeneratedModule(
    postgresGeneratedFiles[0],
    { postgres: { Sql: class Sql {} } }
  );
  const listeners = new Map();
  const writeCallbacks = [];
  const chunks = [];
  let ended = false;
  const writable = {
    write(chunk, callback) {
      chunks.push(String(chunk));
      writeCallbacks.push(callback);
      return false;
    },
    end(callback) {
      ended = true;
      callback();
    },
    once(event, listener) {
      listeners.set(event, listener);
      return this;
    },
    off(event, listener) {
      if (listeners.get(event) === listener) {
        listeners.delete(event);
      }
      return this;
    },
    removeListener(event, listener) {
      return this.off(event, listener);
    },
  };
  const sql = {
    unsafe() {
      return {
        writable() {
          return Promise.resolve(writable);
        },
      };
    },
  };

  const result = copyAuthors(sql, [{ name: "Ann", bio: null }]);
  await flushAsync();
  assert.equal(chunks.length, 1);
  assert.equal(ended, false, "COPY must not end until the backpressured write drains.");

  writeCallbacks[0]();
  listeners.get("drain")?.();
  assert.equal(await result, 1);
  assert.equal(ended, true);
});

