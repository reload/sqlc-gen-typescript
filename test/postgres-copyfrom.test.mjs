function copyfromQuery(text, params = 2) {
  return {
    text,
    name: "CopyAuthors",
    cmd: ":copyfrom",
    columns: [],
    params: Array.from({ length: params }, (_, index) => ({ number: index + 1 })),
    comments: [],
    filename: "query.sql",
    insertIntoTable: { name: "authors", schema: "" },
  };
}

test("copyfrom validation accepts only simple insert value lists", () => {
  const { validateCopyfromQuery } = loadGeneratedModule(copyfromValidationFile);

  assert.doesNotThrow(() =>
    validateCopyfromQuery(copyfromQuery(`
      INSERT INTO authors (
        name, bio
      ) VALUES (
        $1, $2
      );
    `))
  );
  assert.doesNotThrow(() =>
    validateCopyfromQuery(copyfromQuery(`INSERT INTO "authors" ("na)me", bio) VALUES ($1, $2);`))
  );

  for (const sql of [
    `WITH rows AS (SELECT $1) INSERT INTO authors (name, bio) VALUES ($1, $2)`,
    `INSERT INTO authors (name, bio) VALUES ($1, DEFAULT)`,
    `INSERT INTO authors (name, bio) VALUES ($1, lower($2))`,
    `INSERT INTO authors (name, bio) VALUES ($1, 'constant')`,
    `INSERT INTO authors (name, bio) VALUES ($1, $2) RETURNING *`,
    `INSERT INTO authors (name, bio) VALUES ($1, $2) ON CONFLICT DO NOTHING`,
    `INSERT INTO authors (name, bio) VALUES ($1, $2), ($3, $4)`,
  ]) {
    assert.throws(
      () => validateCopyfromQuery(copyfromQuery(sql)),
      /:copyfrom query CopyAuthors must be a simple INSERT INTO table \(columns\.\.\.\) VALUES \(\$1, \.\.\.\) statement/
    );
  }

  assert.throws(
    () =>
      validateCopyfromQuery({
        ...copyfromQuery(`INSERT INTO books (name, bio) VALUES ($1, $2)`),
        insertIntoTable: { name: "authors", schema: "" },
      }),
    /INSERT table must match sqlc table metadata/
  );
});

